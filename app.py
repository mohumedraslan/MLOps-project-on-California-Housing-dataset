import streamlit as st
import joblib
import pandas as pd

# Load model
model = joblib.load('california_house_price_model.pkl')

st.title("California House Price Predictor")
med_inc = st.slider("Median Income", 0.0, 15.0, 3.0)  # Example input
house_age = st.slider("House Age", 0, 60, 30)

# Predict (using dummy values for other features)
input_data = pd.DataFrame([[med_inc, house_age, 0, 0, 0, 0, 0, 0]],
                          columns=['MedInc', 'HouseAge', 'AveRooms', 'AveBedrms',
                                   'Population', 'AveOccup', 'Latitude', 'Longitude'])
prediction = model.predict(input_data)[0]
st.write(f"Predicted Price: ${prediction * 100000:.2f}")

